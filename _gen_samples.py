def build_sheet(ws, sample_key, sample):
    thin = Side(border_style='thin', color='9CA3AF')
    border = Border(left=thin, right=thin, top=thin, bottom=thin)
    header_fill = PatternFill(start_color='1F2937', end_color='1F2937', fill_type='solid')
    cat_fill = PatternFill(start_color='E5E7EB', end_color='E5E7EB', fill_type='solid')
    zero_fill = PatternFill(start_color='FEE2E2', end_color='FEE2E2', fill_type='solid')
    mid_fill = PatternFill(start_color='FEF3C7', end_color='FEF3C7', fill_type='solid')
    full_fill = PatternFill(start_color='DCFCE7', end_color='DCFCE7', fill_type='solid')
    agent_fill = PatternFill(start_color='EFF6FF', end_color='EFF6FF', fill_type='solid')
    customer_fill = PatternFill(start_color='FFFBEB', end_color='FFFBEB', fill_type='solid')
    wrap = Alignment(wrap_text=True, vertical='top')
    center = Alignment(horizontal='center', vertical='center', wrap_text=True)

    # 제목
    ws['A1'] = f'STT 기반 상담 품질 평가표 v3 — {sample_key}'
    ws.merge_cells('A1:F1')
    ws['A1'].font = Font(name='맑은 고딕', bold=True, size=14)
    ws['A1'].alignment = center

    ws['A2'] = f'시나리오: {sample["scenario"]}'
    ws.merge_cells('A2:F2')
    ws['A2'].font = Font(name='맑은 고딕', size=10, color='6B7280')

    ws['A3'] = f'Sample ID: {sample["id"]}  |  Intent: {sample["intent"]}  |  목표 점수: {sample["target"]}점'
    ws.merge_cells('A3:F3')
    ws['A3'].font = Font(name='맑은 고딕', bold=True, size=11, color='1D4ED8')

    # 헤더
    headers = ['대분류', '평가항목', '평가 기준 (세부사항)', '배점', '점수', '판정 근거 + 발화 인용']
    for i, h in enumerate(headers, 1):
        c = ws.cell(row=5, column=i, value=h)
        c.fill = header_fill
        c.font = Font(name='맑은 고딕', bold=True, color='FFFFFF', size=11)
        c.alignment = center
        c.border = border

    # 평가표 row 6~22
    row = 6
    total = 0
    for item_no, cat, name, mx in ITEM_META:
        sc, reason, quotes = sample['scores'][item_no]
        total += sc
        if cat:
            cc = ws.cell(row=row, column=1, value=cat)
            cc.fill = cat_fill
            cc.font = Font(name='맑은 고딕', bold=True, size=10)
            cc.alignment = wrap; cc.border = border
        else:
            ws.cell(row=row, column=1).border = border
        ci = ws.cell(row=row, column=2, value=name)
        ci.font = Font(name='맑은 고딕', size=10); ci.alignment = wrap; ci.border = border
        ccri = ws.cell(row=row, column=3, value=V3_CRITERIA[item_no])
        ccri.font = Font(name='맑은 고딕', size=9); ccri.alignment = wrap; ccri.border = border
        cm = ws.cell(row=row, column=4, value=mx)
        cm.font = Font(name='맑은 고딕', bold=True, size=11); cm.alignment = center; cm.border = border
        cs = ws.cell(row=row, column=5, value=sc)
        cs.font = Font(name='맑은 고딕', bold=True, size=12,
                       color='B91C1C' if sc == 0 else ('92400E' if sc < mx else '166534'))
        cs.alignment = center; cs.border = border
        if sc == 0: cs.fill = zero_fill
        elif sc < mx: cs.fill = mid_fill
        else: cs.fill = full_fill
        memo = f'[판정] {reason}\n' + '\n'.join(' - ' + q_ for q_ in quotes) if quotes else f'[판정] {reason}'
        cmemo = ws.cell(row=row, column=6, value=memo)
        cmemo.font = Font(name='맑은 고딕', size=9); cmemo.alignment = wrap; cmemo.border = border
        lines_c = memo.count('\n') + 1
        ws.row_dimensions[row].height = max(80, min(240, lines_c * 14))
        row += 1

    # 총점
    tot_row = 25
    ws.cell(row=tot_row, column=1, value='총점').font = Font(name='맑은 고딕', bold=True, color='FFFFFF', size=13)
    ws.cell(row=tot_row, column=1).fill = header_fill
    ws.cell(row=tot_row, column=1).alignment = center
    ws.cell(row=tot_row, column=1).border = border
    ws.cell(row=tot_row, column=4, value=100).font = Font(name='맑은 고딕', bold=True, size=13)
    ws.cell(row=tot_row, column=4).alignment = center
    ws.cell(row=tot_row, column=4).border = border
    ws.cell(row=tot_row, column=5, value=total).font = Font(name='맑은 고딕', bold=True, size=13, color='1D4ED8')
    ws.cell(row=tot_row, column=5).alignment = center
    ws.cell(row=tot_row, column=5).border = border
    grade = ('S' if total >= 95 else 'A' if total >= 90 else 'B' if total >= 80
             else 'C' if total >= 70 else 'D')
    ws.cell(row=tot_row, column=6, value=f'등급: {grade}  (95+=S / 90~94=A / 80~89=B / 70~79=C / 70-=D)')
    ws.cell(row=tot_row, column=6).font = Font(name='맑은 고딕', bold=True, size=11)
    ws.cell(row=tot_row, column=6).alignment = wrap
    ws.cell(row=tot_row, column=6).border = border

    # 원문 헤더
    tr_hdr = 27
    ws.merge_cells(start_row=tr_hdr, start_column=1, end_row=tr_hdr, end_column=6)
    hc = ws.cell(row=tr_hdr, column=1, value='═══ 상담 원문 (전체 전사) ═══')
    hc.fill = header_fill
    hc.font = Font(name='맑은 고딕', bold=True, color='FFFFFF', size=13)
    hc.alignment = center

    ws.merge_cells(start_row=tr_hdr + 1, start_column=1, end_row=tr_hdr + 1, end_column=6)
    info = ws.cell(row=tr_hdr + 1, column=1,
                   value='※ 상담사: 파랑  /  고객: 노랑  |  판정 근거의 "상담사#N"은 아래 턴 번호와 매칭')
    info.font = Font(name='맑은 고딕', size=9, color='6B7280')
    info.alignment = Alignment(horizontal='left', vertical='center', wrap_text=True)

    # 원문 본문
    lines = [l.strip() for l in sample['transcript'].strip().split('\n') if l.strip()]
    turn_idx = 0
    for i, line in enumerate(lines):
        r = 29 + i
        if line.startswith('상담사:') or line.startswith('고객:'):
            turn_idx += 1
            tno = ws.cell(row=r, column=1, value=f'#{turn_idx}')
            tno.font = Font(name='맑은 고딕', size=9, color='6B7280', bold=True)
            tno.alignment = center; tno.border = border
            ws.merge_cells(start_row=r, start_column=2, end_row=r, end_column=6)
            tc = ws.cell(row=r, column=2, value=line)
            tc.font = Font(name='맑은 고딕', size=10)
            tc.alignment = Alignment(horizontal='left', vertical='center', wrap_text=True)
            tc.border = border
            if line.startswith('상담사:'):
                tc.fill = agent_fill; tno.fill = agent_fill
            else:
                tc.fill = customer_fill; tno.fill = customer_fill
            ws.row_dimensions[r].height = max(20, min(60, (len(line) // 80 + 1) * 18))

    widths = {1: 12, 2: 22, 3: 46, 4: 7, 5: 7, 6: 66}
    for col, w in widths.items():
        ws.column_dimensions[openpyxl.utils.get_column_letter(col)].width = w

    return total, grade


def write_sample_json(tier_folder, sample_key, sample):
    """사용자 제공 668451*.json 포맷으로 JSON 저장."""
    import json
    tone, brand, scen_slug = SAMPLE_TONES[sample_key]
    site_cd = 'kolon'  # 전 샘플 코오롱 도메인
    # turns 수 계산
    lines = [l.strip() for l in sample['transcript'].strip().split('\n') if l.strip()]
    turns_count = sum(1 for l in lines if l.startswith('상담사:') or l.startswith('고객:'))
    data = {
        'id': sample['id'],
        'label': f'{brand} · {tone}',
        'description': sample['scenario'],
        'turns': turns_count,
        'transcript': sample['transcript'].strip(),
        'SITE_CD': site_cd,
        # 추가 메타: 생성형 샘플 플래그 + 목표 점수 + intent
        'generated_by': 'opus4.7 max',
        'target_score': sample['target'],
        'intent': sample['intent'],
        'tier': sample['tier'],
    }
    # 파일명: (opus4.7 max)_{id}_{site_cd}_{tone}_{brand}_{scen_slug}.json
    fname = f'(opus4.7 max)_{sample["id"]}_{site_cd}_{tone}_{brand}_{scen_slug}.json'
    fpath = os.path.join(tier_folder, fname)
    with open(fpath, 'w', encoding='utf-8') as f:
        json.dump(data, f, ensure_ascii=False, indent=2)
    return fpath


def score_to_tier_folder(total):
    """실제 총점 기준 점수대 폴더명."""
    if total >= 91:
        return '100_91_우수'
    if total >= 81:
        return '90_81_양호'
    if total >= 71:
        return '80_71_보통'
    return '70_60_미흡'


